package com.delta.login;

public class SwitchCase {
	
	public static void main(StringMethods[] args) {
		
		int choice = 2;
		
		switch (choice) {
		
		case 1:
			System.out.println("One");
			break;
			
		case 2:
			System.out.println("Two");
			break;
			
		case 3:
			System.out.println("Three");
			break;
			
			default: 
				System.out.println("Ten");
		
		}
		
	}

}
