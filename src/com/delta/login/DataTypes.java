package com.delta.login;

public class DataTypes {
	
	public static void main(StringMethods[] args) {
		
		// byte - 2 digits
		byte age = 24;
		System.out.println("Age is: "+age);
		
		// short - 4 digits
		short code = 1234;
		System.out.println("Code is:" +code);
		
		// int - 9 digits
		int num = 123456789;
		System.out.println("Number is: "+num);
		
		// long - >9 digits
		long phn = 9876543210l;
		System.out.println("Phn No.: "+phn);
		
		
		// float 
		float avg = 99.89f;
		System.out.println("Avg value is: "+avg);
		
		// double
		double sal = 67895.889;
		System.out.println("Salary is: "+sal);
		
		// char
		char c = 'B';
		System.out.println("Character is: "+c);
		
		// boolean
		boolean b = true;
		System.out.println("Boolean Value is:" +b);
		
		// String
		String str = "hswhj252@i.com";
		System.out.println("Email id: "+str);
	}
	
	
	    
	
}
